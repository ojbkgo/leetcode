func strStr(haystack string, needle string) int {
	next := getNext28(needle)

	var (
		i, j = 0, 0
	)


	for ; i < len(haystack);   {
		if haystack[i] == needle[j] {
			j += 1
			if j == len(needle) {
				return i - j + 1
			}
			i += 1
			continue
		}
		//fmt.Println(haystack[i])

		//fmt.Println(needle[j])
		if j == 0 {
			i += 1
		}

		for j > 0 && haystack[i] != needle[j] {
			j = next[j-1]
		}

	}

	return -1
}

func getNext28(str string) []int {
	n := len(str)
	next := make([]int, n)
	var j = 0
	for i := 1; i < n; i++ {
		for j > 0 && str[j] != str[i] {
			j = next[j-1]
		}

		if str[j] == str[i] {
			j += 1
		}

		next[i] = j
	}

	return next
}

