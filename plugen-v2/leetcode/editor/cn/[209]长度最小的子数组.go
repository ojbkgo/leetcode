func minSubArrayLen(target int, nums []int) int {
	n := len(nums)

	var (
		sum = 0
		res = n + 1
		j = 0
	)

	for i := 0; i < n; i++ {
		sum += nums[i]

		for sum > target {
			sub := i - j + 1
			if res > sub {
				res = sub
			}
			sum -= nums[j]
			j += 1
		}
	}
	if res == n + 1 {
		return 0
	}
	return res
}

func blMinsubarraylen(target int, nums []int) int {
	n := len(nums)
	res := math.MaxInt64
	for i := 0; i < n; i++ {
		sum := 0
		var k int
		for k = i; k < n; k++ {
			sum += nums[k]
			if sum >= target && res > (k - i + 1) {
				res = k - i + 1
				break
			}
		}
	}

	if res == math.MaxInt64 {
		return 0
	}

	return res
}

