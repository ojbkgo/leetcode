func search(nums []int, target int) int {
	//fmt.Println(nums)
	return search33_(nums, 0, len(nums) - 1, target)
}

func search33_(nums []int, l0, r0, target int) int {

	if l0 > r0 {
		return -1
	}

	l, r := l0, r0

	m := l + ((r - l) >> 1)

	if nums[m] == target {
		return m
	} else {
		if nums[m] < nums[r] {
			res := search33(nums, m+1, r, target)
			if res != -1 {
				return res
			}

			res = search33_(nums, l, m-1, target)
			if res != -1 {
				return res
			}
		} else {


			res := search33(nums, l, m-1, target)
			if res != -1 {
				return res
			}

			res = search33_(nums, m+1, r, target)
			if res != -1 {
				return res
			}
		}
	}

	return -1
}

func search33(nums []int, l0, r0, target int) int {

	if len(nums) == -0 {
		return -1
	}

	l, r := l0, r0
	for l <= r {
		m := l + ((r - l) >> 1)

		if nums[m] == target {
			return m
		} else if nums[m] < target {
			l = m + 1
		} else if nums[m] > target {
			r = m - 1
		}
	}

	return -1
}

