/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func mergeTwoLists(list1 *ListNode, list2 *ListNode) *ListNode {
	if list1 == nil {
		return list2
	} else if list2 == nil {
		return list1
	}

	var head *ListNode
	var cur *ListNode

	if list1.Val > list2.Val {
		head = list2
		list2 = list2.Next
	} else {
		head = list1
		list1 = list1.Next
	}

	cur = head

	for list2 != nil || list1 != nil {
		if list1 != nil && list2 != nil {
			if list1.Val < list2.Val {
				cur.Next = list1
				cur = list1
				list1 = list1.Next
			} else {
				cur.Next = list2
				cur = list2
				list2 = list2.Next
			}
		} else if list2 != nil {
			cur.Next = list2
			cur = list2
			list2 = list2.Next
		} else if list1 != nil {
			cur.Next = list1
			cur = list1
			list1 = list1.Next
		}
	}

	return head
}
